public class loops {

    public static void main(String[] args) {

        // print 1 to 9 numbers :-

        int i = 1;

        while (i<=9) {
            
            System.out.println(i);
            i++;   // stopping condition ha loop ki
        }

     //   i++;   // issa infinte ma chala gaya loop 

        System.out.println("Done");
    }
}